object FormatUtil {
    /**
     * convert given time in nanoseconds to a flaoting point number in milliseconds reduced to max 3
     * digits on the right site
     *
     * @param ns
     * @return
     */
    fun formatNSAsMSDouble(ns: Long): Double {
        if (ns >= 100000000L) return (ns / 1000000L).toDouble()
        if (ns >= 10000000L) return ns / 100000L / 10.0
        return if (ns >= 1000000L) ns / 10000L / 100.0 else ns / 1000L / 1000.0
    }
}